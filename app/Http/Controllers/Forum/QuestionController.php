    /**
     * @OA\Post(
     *     path="/sections/{section_id}/lessons/{lesson_id}/questions",
     *     summary="Cria uma questão",
     *     operationId="PostQuestion",
     *     tags={"questions"},
     *     security={{"apiToken":{}}},
     *     @OA\Parameter(
     *         name="section_id",
     *         in="path",
     *         description="Id da sessão",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="lesson_id",
     *         in="path",
     *         description="Id da lição",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="question",
     *         in="query",
     *         description="Descrição da questão",
     *         required=true,
     *         @OA\Schema(
     *           type="string",
     *         )
     *     ),
     *     @OA\Response(
     *         response=200,
     *         description="...",
     *     )
     * )
     */
    public function postQuestion(Request $request, int $section_id, int $lesson_id)
    {
        Section::findOrFail($section_id)
            ->lessons()->findOrFail($lesson_id);

        $request->merge(['user_id' => Auth::user()->id]);
        $request->merge(['lesson_id' => $lesson_id]);

        return parent::store($request); // TODO: Change the autogenerated stub
    }

    /**
     * @OA\Get(
     *     path="/sections/{section_id}/lessons/{lesson_id}/questions/{question_id}",
     *     summary="Lista uma questão",
     *     operationId="GetQuestion",
     *     tags={"questions"},
     *     @OA\Parameter(
     *         name="section_id",
     *         in="path",
     *         description="Id da sessão",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="lesson_id",
     *         in="path",
     *         description="Id da lição",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="question_id",
     *         in="path",
     *         description="Id da questão",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Response(
     *         response=200,
     *         description="...",
     *     )
     * )
     */
    public function getQuestion(int $section_id, int $lesson_id, int $question_id)
    {
        Section::findOrFail($section_id)
            ->lessons()->findOrFail($lesson_id)
            ->questions()->findOrFail($question_id);

        return parent::show($question_id); // TODO: Change the autogenerated stub
    }

    /**
     * @OA\Put(
     *     path="/sections/{section_id}/lessons/{lesson_id}/questions/{question_id}",
     *     summary="Atualiza uma questão",
     *     operationId="PutQuestion",
     *     tags={"questions"},
     *     security={{"apiToken":{}}},
     *     @OA\Parameter(
     *         name="section_id",
     *         in="path",
     *         description="Id da sessão",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="lesson_id",
     *         in="path",
     *         description="Id da lição",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="question_id",
     *         in="path",
     *         description="Id da questão",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="question",
     *         in="query",
     *         description="Descrição da questão",
     *         required=false,
     *         @OA\Schema(
     *           type="string",
     *         )
     *     ),
     *     @OA\Response(
     *         response=200,
     *         description="...",
     *     )
     * )
     */
    public function putQuestion(Request $request, int $section_id, int $lesson_id, int $question_id)
    {
        $user_id = Auth::user()->id;

        Section::findOrFail($section_id)
            ->lessons()->findOrFail($lesson_id)
            ->questions()->findOrFail($question_id)
            ->user()->findOrFail($user_id);

        $request->merge(['user_id' => Auth::user()->id]);
        $request->merge(['lesson_id' => $lesson_id]);

        return parent::update($request, $question_id); // TODO: Change the autogenerated stub
    }

    /**
     * @OA\Delete(
     *     path="/sections/{section_id}/lessons/{lesson_id}/questions/{question_id}",
     *     summary="Apaga uma questão",
     *     operationId="DeleteQuestion",
     *     tags={"questions"},
     *     security={{"apiToken":{}}},
     *     @OA\Parameter(
     *         name="section_id",
     *         in="path",
     *         description="Id da sessão",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="lesson_id",
     *         in="path",
     *         description="Id da lição",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Parameter(
     *         name="question_id",
     *         in="path",
     *         description="Id da questão",
     *         required=true,
     *         @OA\Schema(
     *           type="integer",
     *         )
     *     ),
     *     @OA\Response(
     *         response=200,
     *         description="...",
     *     )
     * )
     */
    public function deleteQuestion(int $section_id, int $lesson_id, int $question_id)
    {
        Section::findOrFail($section_id)
            ->lessons()->findOrFail($lesson_id)
            ->questions()->findOrFail($question_id)
            ->user()->findOrFail(Auth::user()->id);

        return parent::destroy($question_id); // TODO: Change the autogenerated stub
    }
}